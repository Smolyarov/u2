/**
 *
 */
Drivetrain::Drivetrain(void) :
engine(pwm),
servo(pwm)
{
  return;
}

/**
 *
 */
void Drivetrain::start(void) {

  pwm.start();
  engine.start();
  servo.start();

  ready = true;
}

/**
 *
 */
void Drivetrain::stop(void) {
  ready = false;

  servo.stop();
  engine.stop();
  pwm.stop();
}

/**
 *
 */
msg_t Drivetrain::update(const DrivetrainImpact &impact) {

  osalDbgCheck(ready);

  servo.update(impact);
  engine.update(impact);

  return MSG_OK;
}

/**
 *
 */
void Drivetrain::futaba_override(const PwmOverride &override) {
  pwm.futaba_override(override);
}
