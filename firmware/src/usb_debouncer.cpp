UsbDebouncer::UsbDebouncer(void){
  plugged_flag = false;
}

/**
 *
 */
bool UsbDebouncer::update(void){
  bool ret = false;

  if (plugged_flag && (PAL_HIGH == palReadPad(GPIOB, GPIOB_USB_PRESENT)))
    ret = true;

  plugged_flag = PAL_HIGH == palReadPad(GPIOB, GPIOB_USB_PRESENT);

  return ret;
}

